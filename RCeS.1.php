		<h3><b>Luogo e tempo del sacrificio</b></h3>
		<p><em>Il <b>luogo</b> del sacrificio non è sempre un altare adibito a tale funzione. Ad esempio, i sacrifici per i defunti vengono fatti spesso sulle loro tombe, i sacrifici agli spiriti della natura si compiono nei pressi di alberi o cespugli, in grotte, vicino a sorgenti e così via. Spesso fungono da altari pietre naturali o mucchi di pietre o tumuli di terra. Una pietra perpendicolare viene spesso considerata come sede delle divinità e in tal caso il sacrificio viene offerto di fronte alla pietra, non su di essa.<br><br>
			Il <b>tempo</b> per i sacrifi regolari risulta determinato dall'anno astronomico o vegetativo; vi sono pertanto sacrifici giornalieri, settimanali o mensili.<br>
			La semina, il raccolto e il passaggio da una stagione a quella successiva, sono occasioni di sacrificio ampiamente diffuse; nelle culture nomai questo vale soprattutto per la primavera, la stagione in cui nascono gli animali e il latte abbonda. La stagione del raccolto è spesso contraddistinta da sacrifici di primizie che sono considerati condizione necessaria per la desacralizzazione del nuovo raccolto, che solo allora può servire a uso profano.<br>
			La festa del Capodanno spesso non viene stabilità astronomicamente, ma in termini di anno vegetativo.<br>
			Nella vita dell'individuo, nascita, pubertà, matrimonio e morte sono spesso occasioni di sacrificio.<br>
			La commemorazione annuale di un evento storico può diventare occasione di sacrificio.</em></p>
