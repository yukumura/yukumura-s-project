		Le <b>offerte incruente</b> comprendono, in primo luogo, materiali vegetali.<br>
		I popoli <b>raccoglitori</b> offrono una porzione (simbolica) degli alimenti che hanno raccolto.<br>
		I <b>coltivatori</b> offrono alle divinità sacrifici di cibi e di bevande: frutti, tuberi, cereali e alimenti prodotti con tali vegetali (farina, cibo, cotti, olio) insieme a bevande che vengono versate come liberazioni.<br>
